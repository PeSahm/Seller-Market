def test_scheduler_config_default_disabled() -> None:
    out = render_scheduler_config(_ctx())
    parsed = json.loads(out)
    assert parsed == {"enabled": False, "jobs": []}
    # Trailing newline for POSIX hygiene.
    assert out.endswith("\n")


def test_scheduler_config_jobs_round_trip() -> None:
    jobs = (
        SchedulerJobRow(
            name="cache_warmup",
            time="08:00:00",
            enabled=True,
            command="python cache_warmup.py",
        ),
        SchedulerJobRow(
            name="run_trading",
            time="09:00:00",
            enabled=False,
            command="python trading_bot.py --section a1_c2_bbi_IRO1FOLD0001",
        ),
    )
    out = render_scheduler_config(_ctx(scheduler_jobs=jobs, scheduler_enabled=True))

    expected = (
        json.dumps(
            {
                "enabled": True,
                "jobs": [
                    {
                        "name": "cache_warmup",
                        "time": "08:00:00",
                        "command": "python cache_warmup.py",
                        "enabled": True,
                    },
                    {
                        "name": "run_trading",
                        "time": "09:00:00",
                        "command": "python trading_bot.py --section a1_c2_bbi_IRO1FOLD0001",
                        "enabled": False,
                    },
                ],
            },
            indent=2,
        )
        + "\n"
    )
    assert out == expected


