def test_config_ini_empty_when_no_customers() -> None:
    out = render_config_ini(_ctx())
    # Header + DEFAULT section + trailing blank line.
    expected = "# Generated by mgmt_ui — do not edit by hand.\n[DEFAULT]\n"
    assert out == expected


def test_config_ini_renders_customer_sections() -> None:
    cust = CustomerRow(
        section_name="a1_c2_bbi_IRO1FOLD0001",
        username="user1",
        password_plain="hunter2",
        broker="bbi",
        isin="IRO1FOLD0001",
        side=1,
    )
    out = render_config_ini(_ctx(customers=(cust,)))

    assert "[a1_c2_bbi_IRO1FOLD0001]" in out
    assert "username = user1" in out
    assert "password = hunter2" in out
    assert "broker = bbi" in out
    assert "isin = IRO1FOLD0001" in out
    assert "side = 1" in out
    # All five keys present.
    for key in ("username", "password", "broker", "isin", "side"):
        assert f"{key} = " in out


def test_config_ini_renders_multiple_customers() -> None:
    customers = (
        CustomerRow("sec_one", "u1", "p1", "bbi", "IRO1A", 1),
        CustomerRow("sec_two", "u2", "p2", "ayandeh", "IRO1B", 2),
    )
    out = render_config_ini(_ctx(customers=customers))
    assert "[sec_one]" in out
    assert "[sec_two]" in out
    assert out.index("[sec_one]") < out.index("[sec_two]")


