"""Renderer for the per-agent ``config.ini``.

Each customer row becomes a section like::

    [a1_c2_bbi_IRO1FOLD0001]
    username = ...
    password = ...
    broker   = ...
    isin     = ...
    side     = ...

We deliberately do NOT emit a ``[DEFAULT]`` header. The original trading bot
(``SellerMarket/cache_warmup.py`` and ``locustfile_new.py``) reads
``config.sections()`` and iterates per-account configuration — a ``[DEFAULT]``
section adds no value and has caused real-world parser surprises in the bot,
so we skip it. When there are zero customers we emit just the header comment
(``configparser`` parses an empty body fine; ``config.sections()`` returns
``[]``, which is the desired "no accounts" state).
"""

from __future__ import annotations

from app.services.brokers.registry import UnknownBrokerError, family_of
from app.services.rendering import StackRenderContext

# Sentinel marking a COMPLETE config.ini. The bot's auto-sell hot-reload
# supervisor trusts a freshly-read file immediately when it ends with this line
# (a torn in-place write is always a prefix, so it won't have it) — see
# ``SellerMarket/auto_sell_monitor.py::CONFIG_END_SENTINEL``. It's a plain
# comment, ignored by every other config.ini reader, and the bot tolerates its
# absence (older renders) by falling back to a stability re-read.
CONFIG_END_SENTINEL = "# auto-sell-config-end"


def _ini_escape(value: str) -> str:
    """Escape a value for config.ini. The bot's OTHER readers parse the WHOLE
    file with an interpolating ConfigParser (e.g. auto_sell_monitor), which
    raises on a literal ``%``; doubling it keeps them happy. The bot's
    ``runtime_config`` reader undoes this (``%%`` -> ``%``)."""
    return str(value).replace("%", "%%")


def _runtime_lines(ctx: StackRenderContext) -> list[str]:
    """The ``[runtime]`` section lines, or ``[]`` when there are no overrides.

    Deterministic (keys sorted), empty values omitted, every value %%-escaped.
    Emitted as a GLOBAL section (no ``side``/``username``/``auto_sell_threshold``
    keys) so the bot's auto-sell section loop iterates past it harmlessly while
    ``runtime_config`` reads the override values.
    """
    runtime = getattr(ctx, "runtime", None) or {}
    items = sorted((k, v) for k, v in runtime.items() if v not in (None, ""))
    if not items:
        return []
    out = ["", "[runtime]"]
    out.extend(f"{k} = {_ini_escape(v)}" for k, v in items)
    return out


def render_config_ini(ctx: StackRenderContext) -> str:
    """Render ``config.ini`` from the customer rows on the context."""
    lines: list[str] = [
        "# Generated by mgmt_ui — do not edit by hand.",
    ]
    # DB-pushed [runtime] overrides come first (before any customer section and
    # before the sentinel). Omitted entirely when empty so output stays
    # byte-identical to the pre-feature renderer.
    lines.extend(_runtime_lines(ctx))
    if not ctx.customers:
        # Zero-customer stacks get just the header so the file isn't entirely
        # blank (helps diagnostics). The bot's ``config.sections()`` returns
        # ``[]`` either way. The sentinel still terminates it so a disarm-all
        # (config drops to header-only) is trusted + applied fast by the bot.
        lines.append("")
        lines.append(CONFIG_END_SENTINEL)
        lines.append("")
        return "\n".join(lines)
    for c in ctx.customers:
        lines.append("")
        lines.append(f"[{c.section_name}]")
        lines.append(f"username = {c.username}")
        lines.append(f"password = {c.password_plain}")
        lines.append(f"broker = {c.broker}")
        # Data-driven family so the bot routes to the right adapter without a
        # hardcoded enum. Additive + backward-compatible: the current bot image
        # ignores unknown keys, so emitting this can't break existing stacks.
        # Unknown / cold-cache codes default to ephoenix (the legacy behaviour).
        try:
            family = family_of(c.broker)
        except UnknownBrokerError:
            family = "ephoenix"
        lines.append(f"broker_family = {family}")
        lines.append(f"isin = {c.isin}")
        lines.append(f"side = {c.side}")
        # #110 auto-sell: emit only when armed. None AND 0 both mean "disabled"
        # (the service normalizes 0 -> None at storage, and the bot likewise
        # treats threshold <= 0 as disabled), so the truthy guard is the single
        # consistent representation. Additive — the bot ignores unknown keys,
        # so emitting auto_sell_threshold / auto_sell_only can't break older
        # bot images.
        if c.auto_sell_threshold:
            lines.append(f"auto_sell_threshold = {c.auto_sell_threshold}")
        # Auto-sell ONLY (existing holding, no buy): the section keeps side=1
        # so the monitor arms it, but the bot skips it in locust + cache
        # warmup. Emitted ONLY when flagged so unflagged sections render
        # byte-identically to before.
        if c.auto_sell_only:
            lines.append("auto_sell_only = true")
    lines.append("")
    lines.append(CONFIG_END_SENTINEL)  # bot trusts a complete file by this
    lines.append("")  # trailing newline
    return "\n".join(lines)
