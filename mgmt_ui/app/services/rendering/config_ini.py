"""Renderer for the per-agent ``config.ini``.

Each customer row becomes a section like::

    [a1_c2_bbi_IRO1FOLD0001]
    username = ...
    password = ...
    broker   = ...
    isin     = ...
    side     = ...

We deliberately do NOT emit a ``[DEFAULT]`` header. The original trading bot
(``SellerMarket/cache_warmup.py`` and ``locustfile_new.py``) reads
``config.sections()`` and iterates per-account configuration — a ``[DEFAULT]``
section adds no value and has caused real-world parser surprises in the bot,
so we skip it. When there are zero customers we emit just the header comment
(``configparser`` parses an empty body fine; ``config.sections()`` returns
``[]``, which is the desired "no accounts" state).
"""

from __future__ import annotations

from app.services.rendering import StackRenderContext


def render_config_ini(ctx: StackRenderContext) -> str:
    """Render ``config.ini`` from the customer rows on the context."""
    lines: list[str] = [
        "# Generated by mgmt_ui — do not edit by hand.",
    ]
    if not ctx.customers:
        # Zero-customer stacks get just the header so the file isn't entirely
        # blank (helps diagnostics). The bot's ``config.sections()`` returns
        # ``[]`` either way.
        lines.append("")
        return "\n".join(lines)
    for c in ctx.customers:
        lines.append("")
        lines.append(f"[{c.section_name}]")
        lines.append(f"username = {c.username}")
        lines.append(f"password = {c.password_plain}")
        lines.append(f"broker = {c.broker}")
        lines.append(f"isin = {c.isin}")
        lines.append(f"side = {c.side}")
    lines.append("")  # trailing newline
    return "\n".join(lines)
